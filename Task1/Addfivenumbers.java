class SumAndAverageCalculator {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 15;
        int num3 = 20;
        int num4 = 25;
        int num5 = 30;

        int sum = num1 + num2 + num3 + num4 + num5;
        double average = sum / 5;

        System.out.println("Sum: " + sum);
        System.out.println("Average: " + average);
    }
}
